// operators ku waa ay faro badan yihiin waxana jira dhawr nooc oo ay isticmaalan dhamaan programming langauges ka oo dhan walow luuqadaha qaar ay operators ka si kale u yaqaanan 
// 1. Arthematic Operators
// waxa kamid ah kuwan 
// +: addition 
let A = 50;
let B = 50;
let addition = A + B;
console.log(`isku darka xisaabtani waa : ${addition}`);
//  -: substraction 
const C = 40;
const D = 10;
const substraction = C - D;
console.log(`kala jarka xisaabtani waa : ${substraction}`);
//  /:divition
let E = 40;
let F = 2;
let divition = E / F;
console.log(`isku qaybinta Xisaabtani waa : ${divition}`);
// *: Multiplication
let G = 10;
let H = 2;
let Multiplication = G * H;
console.log(`Isku dhufashada Xisaabtani waa : ${Multiplication}`);
// %: Remainder
let I = 12;
let J = 10;
let Remainder = I % J;
console.log(`Xisaabtan marka la qayb qaybiyo waa : ${Remainder}`);
// 2. Logical Operators
// &&: AND
let numberOne = 20;
let numberTwo = 15;
console.log(numberOne > 10 && numberTwo < 16);
// ||: OR 
let numberThree = 30;
let numberFour = 20;
console.log(numberThree < 30 || numberFour > 20);
// == : Equal 
let equlNumber = 50;
console.log(equlNumber == 40);
// !: NOT
let not = 80;
console.log(!not);
// != : NOT Equal 
let notEqual = 80;
console.log(notEqual != 40);
// 3. Comparison Operators
let ComparisonOperator = "Ahmed";
console.log(ComparisonOperator === "Ahmed");
// 4. Assigment Operators : Expresition Operator
